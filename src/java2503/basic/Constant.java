package java2503.basic;

public class Constant {
	
	public static void main(String[] args) {
		
		// 상수는 초기화 해야지만 사용할 수 있음
		final int fi1;
		//System.out.println(fi1);
		
		final int fi2 = 1;
		System.out.println(fi2);
		
		// 상수는 초기화한 후에 값을 변경할 수 없음
		//fi2 = 5;
		
		// 상수명은 대문자로 작성하는 관례가 있음
		final float PI = 3.141592f;
		final int SUM_OF_DAYS = 7;
	}

}










